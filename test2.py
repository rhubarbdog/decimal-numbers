import decimal

zero = decimal.Number(0)
one = decimal.Number(1)
two = decimal.Number(2)

print("one == one", one == one) 
print("one == two", one == two) 
print("one != one", one != one) 
print("one != two", one != two)

print("one < zero", one < zero) 
print("one < one", one < one) 
print("one < two", one < two)

print("one > zero", one > zero) 
print("one > one", one > one) 
print("one > two", one > two)

print("one <= zero", one <= zero) 
print("one <= one", one <= one) 
print("one <= two", one <= two)

print("one >= zero", one >= zero) 
print("one >= one", one >= one) 
print("one >= two", one >= two)

